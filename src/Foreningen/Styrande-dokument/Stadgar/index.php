    <h2 class="kapitel"><a id="kap0--rubrik"></a>Idrottsrörelsens verksamhetsidé</h2>
    <h3 class="paragraf"><a id="kap0-p1"></a>Definition</h3>
    <p>Idrott är fysisk aktivitet som människor utför för att få motion och rekreation eller uppnå tävlingsresultat.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap0-p2"></a>Mål och inriktning</h3>
    <p>Idrott skall inriktas på att utveckla individen positivt i såväl fysisk och psykisk som socialt kulturellt avseende.</p>
    <p>Idrott skall utformas och organiseras så, att den i någon form blir tillgänglig för alla och kan ge välja utövare tillfredställelse med hänsyn till var och ens värderingar.</p>
    <p>Idrott skall organiseras enligt demokratiska principer, varvid individuellt inflytande och ansvarstagande eftersträvas i gemensamma angelägenheter.</p>
    <p>Idrott skall verka som internationellt kontaktmedel och respektera alla människors lika värde.</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <h2 class="kapitel"><a id="kap1--rubrik"></a>1 kap Allmänna bestämmelser</h2>
    <h3 class="paragraf"><a id="kap1-p1"></a>1 § Ändamål</h3>
    <p>Dansklubben Rockrullarna, i dessa stadgar benämnt Föreningen, har som ändamål att främja dansen och danssporten i Sverige genom att bedriva idrottslig verksamhet i enlighet med ”Idrottsrörelsens verksamhetsidé” enligt ovan, med särskild inriktning att vara en tävlings-, motions- och kursinriktad dansförening för barn, ungdomar och vuxna.</p>
    <p>Föreningen ska bedriva följande idrotter: Dans för barn, ungdomar och vuxna.</p>
    <p>Föreningen ska motverka all form av diskriminering samt aktivt verka för en dopingfri idrott.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap1-p2"></a>2 § Föreningens namn m.m.</h3>
    <p>Föreningens fullständiga namn är Dansklubben Rockrullarna.</p>
    <p>Föreningens organisationsnummer är 875001-4147.</p>
    <p>Föreningen har sin hemort/säte i Örebro kommun.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap1-p3"></a>3 § Sammansättning, tillhörighet m.m.</h3>
    <p>Föreningen består av de fysiska personer som har upptagits i föreningen som medlemmar.</p>
    <p>Föreningen är medlem i följande specialidrottsförbund (SF): Svenska Danssportförbundet och är därigenom även ansluten till Sveriges Riksidrottsförbund (RF).</p>
    <p>Genom medlemskap i Svenska Danssportförbundet blir förening även medlem i Örebro Läns Idrottsförbund (DF) samt Bergslagens Danssportförbund (SDF).</p>
    <p>Föreningen är skyldig att följa nämnda organisationers stadgar, tävlingsregler och beslut fattade av dessa idrottsorgan.</p>
    <p>På begäran av RF eller vederbörande SF-, SDF- eller DF-styrelse är föreningen skyldig att ställa föreningens handlingar till förfogande samt lämna av dessa organ begärda uppgifter.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap1-p4"></a>4 § Beslutande organ</h3>
    <p>Föreningens beslutande organ är årsmötet, extra årsmöte och styrelsen.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap1-p5"></a>5 § Verksamhets- och räkenskapsår</h3>
    <p>Föreningens verksamhetsår och räkenskapsår omfattar tiden fr.o.m den 1 januari t.o.m den 31 december.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap1-p6"></a>6 § Firmateckning</h3>
    <p>Föreningens firma tecknas av styrelsen gemensamt.</p>
    <p>Styrelsen har rätt att delegera firmateckningsrätten till två styrelseledamöter gemensamt eller till en eller flera särskilt utsedda personer.</p>
    <p>Den som genom delegation fått fullmakt att företräda föreningen ska återrapportera till styrelsen.</p>
    <h3 class="paragraf"><a id="kap1-p7"></a>7 § Stadgeändring</h3>
    <p>För ändring av dessa stadgar krävs beslut av årsmöte med minst 2/3 av antalet avgivna röster.</p>
    <p>Förslag till ändring av stadgarna får skriftligen avges av såväl medlem som styrelsen.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap1-p8"></a>8 § Tvist/skiljeklausul</h3>
    <p>Talan i tvist där parterna är enskild medlem, funktionär, förening, IdrottsAB, SDF, DF, SF eller RF får inte väckas vid allmän domstol. Sådan tvist ska, utom i fall då annan särskild ordning är föreskriven i RF:s stadgar eller SF:s stadgar, avgöras enligt fastställt reglemente för Idrottens skiljenämnd.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap1-p9"></a>9 § Upplösning av föreningen</h3>
    <p>För upplösning av föreningen krävs beslut av årsmöte med minst 2/3 av antalet avgivna röster.</p>
    <p>I beslut om upplösning av föreningen ska anges dels att föreningens tillgångar ska användas till ett bestämt idrottsfrämjande ändamål, dels var den upplösta föreningens handlingar m.m. ska arkiveras t.ex. i folkrörelsearkiv eller motsvarande.</p>
    <p>Beslutet, tillsammans med kopior av styrelsens och årsmötets protokoll i ärendet, samt revisionsberättelse jämte balans- och resultaträkningar, ska omedelbart skickas till SF.</p>
    <p>&nbsp;</p>
