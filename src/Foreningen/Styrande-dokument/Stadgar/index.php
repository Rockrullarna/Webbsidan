    <h2 class="kapitel"><a id="kap3--rubrik"></a>3 kap Årsmöte</h2>
    <h3 class="paragraf"><a id="kap3-p1"></a>1 § Tidpunkt och kallelse</h3>
    <p>Årsmötet, som är föreningens högsta beslutande organ, hålls före utgången av mars månad på tid och plats som styrelsen bestämmer.</p>
    <p>Kallelse till årsmötet och förslag till föredragningslista ska av styrelsen senast tre veckor före möte tillhandahållas medlemmarna på sätt styrelsen bestämt. Vidare ska kallelse och förslag till föredragningslista publiceras på föreningens hemsida och anslås i klubblokal eller på annan lämplig plats. Har förslag väckts om stadgeändring, nedläggning eller sammanslagning av föreningen med annan förening eller annan fråga av väsentlig betydelse för föreningen eller dess medlemmar ska det anges i kallelsen.</p>
    <p>Verksamhetsberättelse, årsredovisning/årsbokslut, revisorernas berättelser, verksamhetsplan med budget samt styrelsens förslag och inkomna motioner med styrelsens yttrande ska finnas tillgängliga för medlemmarna senast en vecka före årsmötet. I kallelsen ska anges var dessa handlingar finns tillgängliga.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap3-p2"></a>2 § Förslag till ärenden att behandlas av årsmötet</h3>
    <p>Såväl medlem som styrelsen får avge förslag att behandlas av årsmötet.</p>
    <p>Förslag från medlem (motion) ska vara styrelsen tillhanda senast fyra veckor före årsmötet. Styrelsen ska till årsmötet avge skriftligt yttrande över motionerna.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap3-p3"></a>3 § Sammansättning och beslutförhet</h3>
    <p>Årsmöte består av närvarande röstberättigade medlemmar. Vid förfall får medlemmen företrädas av ombud. Ombud får enbart företräda en medlem. Vårdnadshavare har dock rätt att företräda sina omyndiga barn.</p>
    <p>Mötet är beslutsmässigt med de röstberättigade medlemmar och ombud som är närvarande på mötet.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap3-p4"></a>4 § Rösträtt samt yttrande- och förslagsrätt på årsmötet</h3>
    <p>Medlem som har betalat medlemsavgift och under mötesåret fyller lägst 13år har rösträtt på mötet.</p>
    <p>Medlem som inte har rösträtt har yttrande- och förslagsrätt på mötet.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap3-p5"></a>5 § Ärenden vid årsmötet</h3>
    <p>Vid årsmötet ska följande behandlas och protokollföras:</p>
    <ol>
    <li>Fastställande av röstlängd för mötet.</li>
    <li>Val av ordförande och sekreterare för mötet.</li>
    <li>Val av protokolljusterare och rösträknare.</li>
    <li>Fråga om mötet har utlysts på rätt sätt.</li>
    <li>Fastställande av föredragningslista.</li>
    <li>Styrelsens verksamhetsberättelse med årsredovisning/årsbokslut för det senaste verksamhets- /räkenskapsåret.</li>
    <li>Revisorernas berättelse över styrelsens förvaltning under det senaste verksamhets- /räkenskapsåret.</li>
    <li>Fråga om ansvarsfrihet för styrelsen för den tid revisionen avser.</li>
    <li>Fastställande av medlemsavgifter.</li>
    <li>Fastställande av verksamhetsplan samt behandling av ekonomisk plan för kommande verksamhets-/räkenskapsår.</li>
    <li>Behandling av styrelsens förslag och i rätt tid inkomna motioner.</li>
    <li>Val av<ol style="list-style-type: lower-alpha;">
    <li>föreningens ordförande för en tid av två (2) år (jämna år);</li>
    <li>tre (3) ledamöter i styrelsen för en tid av två (2) år;</li>
    <li>två (2) suppleanter (ersättare) i styrelsen för en tid av ett (1) år;</li>
    <li>två (2) revisorer jämte suppleanter (ersättare) för en tid av ett (1) år. I detta val får inte styrelsens ledamöter delta;</li>
    <li>tre (3) ledamöter i valberedningen för en tid av ett (1) år, av vilka en ska utses till ordförande.</li>
    </ol></li>
    <li>Eventuella övriga frågor som anmälts under punkt 5. Beslut i fråga av större ekonomisk eller annan avgörande betydelse för föreningen eller medlemmarna får inte fattas om den inte varit med i kallelsen till mötet.</li>
    </ol>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap3-p6"></a>6 § Valbarhet</h3>
    <p>Valbar till styrelsen och valberedningen är röstberättigad medlem av föreningen. Arbetstagare inom föreningen får dock inte väljas till ledamot eller suppleant i styrelsen, valberedningen eller till revisor eller revisorssuppleant i föreningen.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap3-p7"></a>7 § Extra årsmöte</h3>
    <p>Styrelsen kan kalla medlemmarna till extra årsmöte.</p>
    <p>Styrelsen är skyldig att kalla till extra årsmöte när en revisor eller minst en tiondel av föreningens röstberättigade medlemmar begär det. Sådan framställning ska avfattas skriftligen och innehålla skälen för begäran.</p>
    <p>När styrelsen mottagit en begäran om extra årsmöte ska den inom 14 dagar utlysa sådant möte att hållas inom två månader från erhållen begäran.</p>
    <p>Kallelse med förslag till föredragningslista för extra årsmöte ska senast sju dagar före mötet tillställas medlemmarna på sätt styrelsen bestämt. Vidare ska kallelse och förslag till föredragningslista publiceras på föreningens hemsida och anslås i klubblokal eller på annan lämplig plats.</p>
    <p>Underlåter styrelsen att utlysa eller kalla till extra årsmöte får de som gjort framställningen vidta åtgärder enligt föregående stycke.</p>
    <p>Vid extra årsmöte får endast den eller de frågor som angetts i förslaget till föredragningslista behandlas.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap3-p8"></a>8 § Beslut och omröstning</h3>
    <p>Beslut fattas med bifallsrop (acklamation) eller om så begärs efter omröstning (votering).</p>
    <p>Med undantag för de i 1 kap. 7 § första stycket och 1 kap. 9 § nämnda fallen avgörs vid omröstning alla frågor genom enkel majoritet. Enkel majoritet kan vara antingen absolut eller relativ.</p>
    <p>Val avgörs genom relativ majoritet. Med relativ majoritet menas att den (de) som erhållit högsta antalet röster är vald (valda) oberoende av hur dessa röster förhåller sig till antalet avgivna röster.</p>
    <p>För beslut i andra frågor än val krävs absolut majoritet, vilket innebär mer än hälften av antalet avgivna röster.</p>
    <p>Omröstning sker öppet. Om röstberättigad medlem begär det ska dock val ske slutet.</p>
    <p>Vid omröstning som inte avser val gäller vid lika röstetal det förslag som biträds av ordföranden vid mötet, om ordföranden är röstberättigad. Är mötesordföranden inte röstberättigad avgör lotten. Vid val ska i händelse av lika röstetal lotten avgöra.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap3-p9"></a>9 § Ikraftträdande</h3>
    <p>Beslut fattade av årsmöte gäller från årsmötets avslutande om inte annat sägs.</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <h2 class="kapitel"><a id="kap4--rubrik"></a>4 kap Valberedning</h2>
    <h3 class="paragraf"><a id="kap4-p1"></a>1 § Sammansättning</h3>
    <p>Valberedningen ska bestå av ordförande och två (2) övriga ledamöter valda av årsmötet. Valberedningen ska bestå av kvinnor och män, och olika åldersgrupper ska finnas representerade.</p>
    <p>Valberedningen ska bland sina ledamöter utse en vice ordförande. Valberedningen ska sammanträda när ordföranden eller minst halva antalet ledamöter så bestämmer.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap4-p2"></a>2 § Åligganden</h3>
    <p>Valberedningen ska bereda valen inför kommande årsmöte, och ska i detta arbete fortlöpande under verksamhetsåret följa styrelsens och revisorernas arbete.</p>
    <p>Valberedningen ska senast två månader före årsmötet tillfråga dem vilkas mandattid utgår vid mötets slut, om de vill kandidera för nästa mandattid. Därefter ska valberedningen informera medlemmarna om eventuella avsägelser. Valberedningen ska upplysa medlemmarna om att de har rätt att inkomma med förslag på kandidater.</p>
    <p>Senast tre veckor före årsmötet ska valberedningen meddela röstberättigade medlemmar sitt förslag, samt meddela namnen på de personer som i övrigt har föreslagits inför valberedningen.</p>
    <p>Innan kandidatnominering påbörjas på årsmötet ska valberedningen meddela sitt förslag beträffande det val nomineringen avser.</p>
    <p>De som ingår i valberedningen får inte obehörigen röja vad de i denna egenskap fått kännedom om.</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <h2 class="kapitel"><a id="kap5--rubrik"></a>5 kap Revision</h2>
    <h3 class="paragraf"><a id="kap5-p1"></a>1 § Revisorer och revision</h3>
    <p>Föreningens räkenskaper och förvaltning ska årligen granskas av de av årsmötet utsedda revisorerna.</p>
    <p>Revisorerna ska vara oberoende av dem som de har att granska.</p>
    <p>Revisorerna har rätt att fortlöpande ta del av föreningens räkenskaper, årsmötes- och styrelseprotokoll och övriga handlingar.</p>
    <p>Föreningens räkenskaper för det senaste verksamhets- och räkenskapsåret ska vara revisorerna tillhanda senast en månad före årsmötet.</p>
    <p>Revisorerna ska granska styrelsens förvaltning och räkenskaper för det senaste verksamhets- och räkenskapsåret samt till styrelsen överlämna revisionsberättelse senast 14 dagar före årsmötet.</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <h2 class="kapitel"><a id="kap6--rubrik"></a>6 kap Styrelsen</h2>
    <h3 class="paragraf"><a id="kap6-p1"></a>1 § Sammansättning</h3>
    <p>Styrelsen ska bestå av ordförande samt minst sex (6) övriga ledamöter. Styrelsen ska bestå av kvinnor och män.</p>
    <p>Styrelsen ska inom sig utse vice ordförande sekreterare, kassör och de övriga befattningshavare som behövs.</p>
    <p>Vid förhinder för ledamot ersätts ledamoten av suppleant. Om ledamot avgår i förtid ersätter suppleanten ledamoten för tiden t.o.m. nästföljande årsmöte.</p>
    <p>Styrelsen får utse adjungerad ledamot. Sådan ledamot har yttrande- och förslagsrätt men inte rösträtt. Adjungerad ledamot får utses till befattning inom styrelsen.</p>
    <p>Styrelsen får utse ombud till möten där föreningen har rätt att vara representerad genom ombud.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap6-p2"></a>2 § Styrelsens åligganden</h3>
    <p>När årsmöte inte är samlat är styrelsen föreningens beslutande organ och ansvarar för föreningens angelägenheter.</p>
    <p>Styrelsen ska – inom ramen för RF:s, SF:s och förevarande stadgar – svara för föreningens verksamhet samt tillvarata medlemmarnas intressen.</p>
    <p>Det åligger styrelsen särskilt att</p>
    <ul>
    <li>se till att föreningen följer gällande författningar och andra bindande regler,</li>
    <li>verkställa av årsmötet fattade beslut,</li>
    <li>planera, leda och fördela arbetet inom föreningen och för detta utarbeta arbetsordning samt instruktioner för underliggande föreningsorgan,</li>
    <li>ansvara för och förvalta föreningens medel,</li>
    <li>fortlöpande informera medlemmarna om föreningens angelägenheter i den utsträckning detta inte kan skada föreningens intressen,</li>
    <li>tillställa revisorerna räkenskaper m.m. enligt 5 kap. 1 §, och</li>
    <li>förbereda årsmöte.</li>
    </ul>
    <p>Ordföranden ska leda styrelsens arbete och bevaka att styrelsen fullgör sina uppgifter enligt föreningens stadgar och övriga för föreningen bindande regler och beslut. Har ordföranden förhinder ska vice ordföranden träda in i ordförandens ställe.</p>
    <p>Styrelsen ska besluta om fördelning av arbetsuppgifterna i övrigt genom en särskild upprättad arbetsordning.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap6-p3"></a>3 § Kallelse, beslutförhet och omröstning</h3>
    <p>Styrelsen ska sammanträda på kallelse av ordföranden. Ordföranden är skyldig att kalla till sammanträde då minst två ledamöter har begärt det. Underlåter ordföranden att utfärda kallelse får de som gjort framställningen kalla till sammanträde.</p>
    <p>Styrelsen är beslutsmässig när samtliga ledamöter kallats och då minst halva antalet ledamöter är närvarande. För alla beslut krävs att minst hälften av styrelsens samtliga ledamöter är ense om beslutet. Vid lika röstetal har ordföranden utslagsröst. Röstning får inte ske genom ombud.</p>
    <p>Ordföranden får besluta att ärende ska avgöras genom skriftlig omröstning (beslut per capsulam) eller vid telefonsammanträde eller med hjälp av annan teknisk utrustning.</p>
    <p>Vid sammanträde eller beslutsfattande enligt föregående stycke ska protokoll upprättas. Protokoll ska undertecknas av mötesordföranden och utsedd protokollsekreterare. Avvikande mening ska antecknas i protokollet.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap6-p4"></a>4 § Överlåtelse av beslutanderätten</h3>
    <p>Styrelsen får överlåta sin beslutanderätt i enskilda ärenden eller i vissa grupper av ärenden till kommitté eller annat organ eller till enskild medlem, anställd eller annan utsedd person.</p>
    <p>Den som fattat beslut med stöd av bemyndigande enligt föregående stycke ska fortlöpande underrätta styrelsen härom.</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <h2 class="kapitel"><a id="kap7--rubrik"></a>7 kap Övriga föreningsorgan</h2>
    <h3 class="paragraf"><a id="kap7-p1"></a>1 § Kommittéer, arbetsgrupper och andra underliggande föreningsorgan</h3>
    <p>Beslut om bildande eller nedläggning av kommittéer, arbetsgrupper och andra underliggande föreningsorgan fattas av föreningens årsmöte eller extra årsmöte som sammankallats för prövning av sådan fråga.</p>
    <p>Styrelsen får härutöver vid behov inrätta tillfälliga kommittéer och arbets- och projektgrupper.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap7-p2"></a>2 § Instruktioner</h3>
    <p>Föreningens styrelse ska, i särskild instruktion eller på annat lämpligt sätt, fastställa de befogenheter och skyldigheter som de underliggande organen har.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap7-p3"></a>3 § Budget och verksamhetsplan</h3>
    <p>Sektion/kommitté för respektive idrottsverksamhet samt övriga fasta kommittéer ska upprätta förslag till budget och verksamhetsplan för sektionen/kommittén att gälla under nästföljande verksamhetsår. Budget och planen inges till styrelsen för godkännande på tid som den bestämmer. Styrelsen gör de ändringar i budgeten och planen som bedöms vara nödvändiga med hänsyn till föreningens skyldigheter i olika hänseenden, föreningens ekonomiska ställning eller den väntade utvecklingen av den idrottsliga verksamheten.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap7-p4"></a>4 § Återrapportering</h3>
    <p>Den som fått bemyndigande av styrelsen enligt 6 kap. 4 § ska fortlöpande underrätta styrelsen genom återrapportering i den ordning som styrelsen fastställt i instruktion.</p>
