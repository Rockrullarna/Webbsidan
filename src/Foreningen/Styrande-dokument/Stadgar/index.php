    <h2 class="kapitel"><a id="kap2--rubrik"></a>2 kap Föreningens medlemmar</h2>
    <h3 class="paragraf"><a id="kap2-p1"></a>1 § Medlemskap</h3>
    <p>Alla personer som vill stödja föreningens ändamål har rätt att beviljas medlemskap. Ansökan om medlemskap får avslås endast om det kan antas att sökanden kommer att motarbeta föreningens ändamål, idrottens värdegrund eller på annat sätt skada föreningens intressen.</p>
    <p>Medlemskap beviljas av styrelsen eller av den som styrelsen delegerat beslutanderätten till.</p>
    <p>Medlemskapet omfattar tiden fr.o.m den 1 januari t.o.m den 31 december.</p>
    <p>Beslut att avslå medlemsansökan ska fattas av styrelsen eller av den som styrelsen delegerat beslutanderätten till. Innan sådant beslut fattas ska personen i fråga ges tillfälle att inom viss tid, minst 14 dagar, yttra sig över de omständigheter som är anledningen till att medlemskap ifrågasätts.</p>
    <p>I beslutet att avslå medlemsansökan ska skälen redovisas samt anges vad den medlemssökande ska iaktta för att överklaga beslutet. Beslutet ska inom tre dagar från dagen för beslutet skriftligen skickas till den som fått avslag på medlemsansökan.</p>
    <p>Beslut att avslå medlemsansökan får överklagas av den berörde inom tre veckor till SF.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap2-p2"></a>2 § Medlems skyldigheter och rättigheter</h3>
    <p>Medlem</p>
    <ul>
    <li>ska följa föreningens stadgar och beslut som fattats av föreningsorgan samt följa i 1 kap 3 § nämnda organisationers stadgar, tävlingsregler och beslut,</li>
    <li>ska betala de avgifter som beslutats av föreningen,</li>
    <li>godkänner genom sitt medlemskap att föreningen får behandla personuppgifter i syfte att bedriva ändamålsenlig verksamhet i enlighet med vid var tid gällande föreningsstadgar och i enlighet med eventuella övriga villkor för behandling av personuppgifter som beslutats av föreningen,</li>
    <li>har rätt att delta i sammankomster som anordnas för medlemmarna,</li>
    <li>har rätt till information om föreningens angelägenheter i den utsträckning som följer av 6 kap 2 §,</li>
    <li>har inte rätt att ta del av föreningens behållning eller egendom vid upplösning av föreningen.</li>
    </ul>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap2-p3"></a>3 § Medlems deltagande i tävlingsverksamhet</h3>
    <p>Medlem har rätt att delta i föreningens idrottsliga verksamhet under de former som är vedertagna inom idrotten och på samma villkor som gäller för övriga medlemmar. Föreningen har därvid rätt att uppställa särskilda villkor för utövande av vissa uppdrag.</p>
    <p>Vid deltagande i tävling eller uppvisning representerar medlem sin förening.</p>
    <p>Föreningen bestämmer förutsättningarna för medlems deltagande i tävling eller uppvisning. För deltagande i tävling eller uppvisning utanför Sverige krävs SF:s godkännande.</p>
    <p>Är arrangören av tävlingen eller uppvisningen inte ansluten till det SF som administrerar ifrågavarande idrottsgren, får medlemmen delta endast om detta SF godkänt tävlingen eller uppvisningen.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap2-p4"></a>4 § Utträde</h3>
    <p>Medlem, som önskar utträda ur förening, ska skriftligen anmäla detta.</p>
    <p>Om medlem inte har betalat medlemsavgiften inom tre (3) månader efter sista betaldatum, får anses ha begärt sitt utträde ur föreningen.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap2-p5"></a>5 § Uteslutning m.m.</h3>
    <p>Medlem får, utan iakttagande av 4 § andra stycket, uteslutas om medlemmen, trots påminnelser, har försummat att betala av föreningen beslutade avgifter. Medlem får också uteslutas om medlemmen motarbetat föreningens verksamhet eller ändamål, brutit mot föreningens stadgar, idrottens värdegrund eller på annat sätt skadat föreningens intressen.</p>
    <p>Uteslutning gäller tills vidare. Beslut om uteslutning får dock begränsas till att omfatta viss tid. Sådan tidsbegränsad uteslutning får som mest omfatta sex månader från beslutsdagen.</p>
    <p>Om tillräckliga skäl för uteslutning inte föreligger får föreningen i stället meddela medlemmen varning.</p>
    <p>Beslut om uteslutning eller varning får inte fattas utan att medlemmen inom viss av föreningsstyrelsen angiven tid, minst 14 dagar, fått tillfälle att yttra sig över de omständigheter som föranlett att medlemskapet ifrågasätts.</p>
    <p>I beslut om uteslutning eller varning ska skälen redovisas samt anges vad medlemmen ska iaktta för överklagande av beslutet. Beslutet ska inom tre dagar från dagen för beslutet skickas till medlemmen.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap2-p6"></a>6 § Överklagande</h3>
    <p>Beslut om att vägra medlemskap, medlemskapets upphörande eller varning får överklagas till SF enligt reglerna i 15 kap. RF:s stadgar.</p>
    <p>&nbsp;</p>
    <h3 class="paragraf"><a id="kap2-p7"></a>7 § Medlemskapets upphörande</h3>
    <p>Beslut om upphörande av enskilds medlemskap gäller inte förrän klagotiden utgått, eller då beslut överklagats, ärendet blivit slutligt avgjort.</p>
    <p>&nbsp;</p>
