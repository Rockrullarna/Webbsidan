  <p>
    Varje kommitté ska arbeta som ett team i alla frågor, och alla i kommittén ska 
    kunna ta vid för varandra. Men de kommer att ha utsedda ansvariga inom olika 
    områden som marknadsföring, tävling och danskvällar. Dessa områdesansvariga 
    samarbetar med motsvarande områdesansvariga inom de andra kommittéerna, och bildar 
    områdes-kommittéer (t.ex. marknadsföringskommittén). Avgränsade ansvarsområden kan 
    även tilldelas andra medlemmar utanför kommittén. Roller som sammanlänkar områdes-kommittéer, 
    verksamheten och styrelsen är bl.a. huvudtränaransvarig, kursadministratör, 
    samordnande marknadsföringsansvarig och kommunikatör, dessa roller innehas ofta av personer
    som redan sitter på andra ansvarsroller och har nära kontakt med verksamheten.
  </p>
