  <h1>
    Information om ny organisationsstruktur
  </h1>
  <p>
    Vi är i startgroparna av en ny organisationsstruktur inom föreningen. 
    Ansvariga och drivande personer inom våra respektive danssektioner, 
    Bugg, Fox, WCS och Ungdom, kommer att ingå i kommittéer (arbetsgrupper) 
    för sin respektive danssektion, enligt belsut på 2024 års årsmöte. 
    En sektion för 65+ tittas på. Utbildning i styrelsearbete planeras.
  </p>
  <p>
    Bakgrunden till denna nya organisationsstruktur är att det tidigare 
    tillvägagångssättet visat sig skört. Ansvars har fallit på enskilda 
    personer som ibland haft flera uppdrag som den varit tvungen att driva 
    enskilt. När en person slutat så har avgörande positioner stått vakanta 
    och verksamheten drabbats. Tanken med kommitté-strukturen är att en 
    person inte ensam ska behöva sitta på ett uppdrag utan att man alltid 
    är en grupp av flera personer som hjälps åt. Det ska inte vara betungande 
    att vara engagerad hos oss, tvärtom! 
  </p>
