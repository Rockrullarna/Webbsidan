  <p>
    <b>Kommittéernas uppdrag</b> är att vara en samlad kompetens inom sin dansstil 
    i föreningen och att bl.a. bidra i föreningens verksamhetsplanering. 
    De ska ha kunskap om bl.a. nyheter och events i närområdet under året 
    inom sin dansstil. En kommitté ska ha en samordnande funktion för, och 
    ett mycket nära samarbete med, övriga engagerade medlemmar inom danssektionen. 
    Danssektionernas kommittéer ska samarbeta med varandra och en representant 
    ifrån varje kommitté (roterande eller fast) ska delta på varje styrelsemöte. 
    Detta innebär mer direktkontakt - mellan kommittéerna och styrelsen, samt 
    kommittéerna emellan. 
  </p>
