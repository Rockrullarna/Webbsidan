    <h1>Privatlektioner</h1>
    <p>
      Privatlektioner med föreningens interna tränare ses som ett komplement och är tillåtet med följande förhållningsregler:
    </p>
    <ul>
      <li>Privatlektioner är inte en del av ordinarie verksamhet, utan något som är upp till varje tränare att erbjuda i mån av tid och möjlighet.</li>
      <li>Styrelsen utser ej vilken tränare som ska ge privatlektioner. Det är upp till den enskilde medlemmen att kontakta den tränare som man önskar privatlektion av.</li>
      <li>Med privatlektion avses lektion för ett danspar och inte en grupp.</li>
      <li>Samtliga deltagare måste vara medlemmar i DK Rockrullarna och delta på minst en ordinarie danskurs.</li>
    </ul>
    <p>
      Observera att föreningen har inget utbildningsmaterial som specifikt riktar sig för privatlektioner.
    </p>
    <h2>Betalning och priser</h2>
    <p>Betalning måste gå via föreningen och får ej ske direkt mellan deltagare och instruktör.</p>
    <ul>
      <li>Pris för privatlektion med <strong>1 instruktör: 500 SEK/timme</strong>.</li>
      <li>Pris för privatlektion med <strong>2 instruktörer: 800 SEK/timme</strong>.</li>
      <li>Betalning sker via <strong>Swish (1232220283)</strong> eller <strong>Bankgiro (865-0277)</strong> med följande meddelande:
        <ul>
          <li><p>"<em>Privatkurs med: Tränarens namn</em>" <strong>Exempel</strong>: "Privatkurs med: Karl Karlsson"</p></li>
        </ul>
      </li>
    </ul>

