    <h1>
      Betalning
    </h1>
    <h2>Villkor</h2>
    <p>
      Anmälan till föreningens aktiviteter är bindande inom 14 dagar före start (gäller ej nybörjarkurs med ”prova på”). Vid sjukdom kan återbetalning endast genomföras vid uppvisande av läkarintyg.
    </p>
    <ul>
      <li>Danskurser ska betalas innan första kurstillfället.</li>
      <li>Nybörjarkurser med ”prova på” ska betalas till nästkommande danskurstilfälle.</li>
    </ul>
    <h2>Betalningsinformation</h2>
    <p>
      Betalningsinformation för kurs/medlemskap framgår i antagningsbeskedet som skickas ut via e-post.<br />
      Betalning görs till <strong>bankgiro: 865-0277</strong> tillsammans med OCR-numret som anges via betalningslänken i antagningsbeskedet.
    </p>
    <p>
      Observera att betalning ska ej genomföras innan du fått ditt antagningsbesked.
    </p>
    <p>
      När vi mottagit din betalning skickas ett kvitto ut via e-post.
    </p>
    <h2>Friskvård</h2>
    <p>
      Betalning av våra danskurser via friskvård är fullt möjligt via Epassi, alternativt att du skickar in kvittot till din arbetsgivare. <br />
      OBSERVERA, att du INTE kan betala medlemsavgiften med friskvård.
    </p>
    <h3>Epassi</h3>
    <p>
      Betalning via Epassi genomförs av enskild medlem.<br />
      Instruktioner för betalning via <a href="http://www.epassi.se/">Epassi appen/hemsidan</a>, hittar du på sidan: 
      <a href="https://www.epassi.se/sv/anstalld/friskvard/faq?path=%2Fguide%2Fhur-genomfor-jag-ett-kop-med-epassi%3Fcategory%3Dbetala-med-epassi">www.epassi.se/sv/anstalld/friskvard/faq</a>
    </p>
    <p>
      Vid betalning via Epassi kan betalningen till DK Rockrullarna dröja vilket kan leda till att din ankomstregistrering kan vara missvisande (displayen på blir orange och du blir ombedd att kontakta personal). Vi ber er ha överseende med detta.
    </p>
    <p>
      Kvitto på utförd betalning kommer via e-post, så fort vår kassör har registrerat din friskvårdsbetalning korrekt. Vid frågor om din friskvårdsbetalning, kontakta kassören på: <a href="mailto:ekonomi@rockrullarna.se">ekonomi@rockrullarna.se</a>.
    </p>
    <h2>Café</h2>
    <p>
      Betalning vid café görs via <strong>Swish: 1232220283</strong> eller med kontanter. Om du swishar, märk betalningen med "Fika", eller liknande. 
    </p>
