    <h3 class="avsnitt"><a id="kap7-1"></a>7.1 Bugg</h3>
    <p>Bugg är en vanlig svensk pardans som dansas till musik i fyrafjärdedelstakt i vanligtvis snabbt tempo, 120–180 BPM. Dansen är helt improviserad där föraren har till uppgift att föra följaren i olika snurrar, turer och kombinationer. Bugg tillhör danskategorin swing och utförs i både sociala och i tävlingssammanhang.</p>
    <p>Föreningen erbjuder allt från grundkurser till tävlingsträning.</p>

    <h3 class="avsnitt"><a id="kap7-2"></a>7.2 West Coast Swing</h3>
    <p>West Coast Swing (WCS) är en cool pardans med rötter i Lindy hop och passar till bland annat musik så som radiohits, pop, slow rock, blues, R&B, hiphop och ballader.</p>
    <p>Föreningen erbjuder allt från grundkurser till tävlingsförberedande träning.</p>

    <h3 class="avsnitt"><a id="kap7-3"></a>7.3 Fox</h3>
    <p>Fox, är en modern utveckling av den traditionella dansen Foxtrot. Det är en pardans med fokus på samspel i paret genom att aktivt arbeta med känsla, balans, hållning och närvaro. Grundstegen utförs i quicksteg och slowsteg. Med fria steg sätter föraren ihop sin egen dans utifrån vad du hör och känner i musiken. Den moderna foxen har influenser från flera dansstilar till exempel Kizomba.</p>
    <p>Föreningen erbjuder allt från grundkurser till mer avancerade kurser.</p>

    <h3 class="avsnitt"><a id="kap7-4"></a>7.4 Dans för barn och ungdom</h3>
    <p>DK Rockrullarnas erbjuder varierande kurser för barn och ungdomar upp till 17år. Här får barn och ungdomar utlopp för energi genom dans och rörelse med fokus på att ha roligt.</p>
    <p>För aktuella kurser hänvisas till hemsidan.</p>

