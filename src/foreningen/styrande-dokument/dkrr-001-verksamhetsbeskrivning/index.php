    <h2 class="kapitel"><a id="kap18"></a>18 Medlemsrabatter</h2>

    <h3 class="avsnitt"><a id="kap18-1"></a>18.1 Kurser</h3>
    <p>Föreningens danskurser är populära vilket innebär att det ibland bildas köer. Statistiskt sett är det oftast bristen på förare som orsakar köer bland följare. För att minska köer i de fall där fördelningen mellan förare och följare är ojämn kan ett rabattsystem tillämpas. Rabattsystemet används för att locka förare eller följare att anmäla sig till kurser som har behov av att jämna ut fördelningen. Kursansvarig tillsammans med tränaransvarig avgör om, när och vilken rabatt som ska tillämpas.</p>
    <p>Information om vilka kurser som är/blir rabatterade samt om rabatten gäller förare eller följare förmedlas via mejl, sociala media samt hemsida.</p>

