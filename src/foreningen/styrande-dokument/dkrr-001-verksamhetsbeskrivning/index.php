    <p>Observera att du inte kan betala medlemsavgiften med friskvård, detta enligt skatteregler. Om medlemsavgiften ändå betalats in via Epassi kommer summan flyttas över som tillgodohavande för medlemmen (medlemmen behöver fullgöra betalningen av medlemsavgiften)</p>
    <ul>
      <li>Betalning via Epassi genomförs av enskild medlem. Instruktioner för betalning via Epassi hittas på: <a href="https://www.epassi.se">www.epassi.se</a></li>
    </ul>
    <p>Vid betalning via Epassi kan betalningen till DK Rockrullarna dröja vilket kan leda till att din ankomstregistrering kan vara missvisande (displayen på blir orange och du blir ombedd att kontakta personal). Vi ber er ha överseende med detta.</p>

    <h3 class="avsnitt"><a id="kap8-3"></a>8.3 Inställd kurs och återbetalning</h3>
    <p>Kurser kan behöva ställas in på grund av oförsägbara händelser eller att det är för få anmälda deltagare. Antalet deltagare till en terminskurs bör som minst vara 8–10 danspar. Beslutet om att ställa in kurs fattas av respektive danskommitté.</p>
    <p>Vid händelse av inställd kurs är ambitionen att informera deltagare via mejl och hemsida senast tre dagar innan kursstart.</p>
    <p>Om deltagaren redan har betalat kursavgiften kommer det inbetalade beloppet finnas kvar som tillgodohavande hos Rockrullarna. Vill deltagaren ha återbetalning av kursavgiften kontaktas kassören via ekonomi@rockrullarna.se</p>

