    <h2 class="kapitel"><a id="kap1"></a>1 Inledning</h2>
    <p>För Dansklubben Rockrullarna betyder dans, glädje och gemenskap. Dansen för människor samman, stimulerar och utvecklar oss på många olika plan. I dansen kan vi vara här och nu, vi tillåts lägga allt annat åt sidan för en stund. Vi vill dela med oss av detta.</p>
    <p>Dansklubben Rockrullarna har drivits av medlemmar, för medlemmar i över 40 år.</p>
    <p><strong>Citat från medlemmar:</strong></p>
    <ul>
      <li>"Jag har varit i Brunnsparken innan och dansat men efter den här kursen känner jag att jag fått de verktyg jag behöver för att dansa på riktigt."</li>
      <li>"Roligt att dansa med en positiv atmosfär och fin sammanhållning. Även om jag varit trött när jag kommit dit har jag gått därifrån glad och med ny energi."</li>
      <li>"Pedagogiska och professionella ledare som dessutom utstrålar värme och medmänsklighet"</li>
      <li>"Duktig personal och dansare. Jag har dansat sedan jag var 16 år och jag är nu 70 år"</li>
    </ul>

    <h3 class="avsnitt"><a id="kap1-1"></a>1.1 Bakgrund</h3>
    <p>Dansklubben Rockrullarna (DKRR) startades den 23 oktober 1983 där Ulf Bergentoft var en av grundarna. Kursverksamheten påbörjades 1985 där föreningen bestod av en grupp vänner i åldern 20–30 år som studerande på Gymnastik och Idrottshuset (GIH) i Örebro. Träningar och allt som tillkom bekostades av dem själva. Externa tränare anlitades vid enstaka tillfällen och tränade då en större mängd dansare vid ett och samma tillfälle. År 1984 representerandes Rockrullrana på tävlingsdansgolvet i grenarna Bugg, Rock 'n' Roll och Jitterbugg. Från 1984 och parallellt med tävlingsdansen har föreningen alltid sysslat med dansuppvisningar och, under perioder, även med formationsdans. Mellan åren 1985 till 1996 har föreningen alltid haft minst en representant i Danssportförbundet, och då i förbunds- eller sektionsstyrelsen. Föreningens första ordförande var Per-Axel Svensson.</p>
    <p>År 1998 bestämde föreningen att satsa på en egen lokal och 1999 stod det klart att kursverksamheten skulle äga rum i Haga Centrum där föreningen än idag genomför sin verksamhet.</p>

    <h3 class="avsnitt"><a id="kap1-2"></a>1.2 Syfte</h3>
    <p>Syftet med detta dokument är att beskriva den verksamhet som bedrivs av Dansklubben Rockrullarna. Dokumentet ska kunna användas av föreningens medlemmar för att besvara vanligt förekommande frågor samt att ge information om vilka riktlinjer och policys som gäller för verksamheten.</p>
    <p>Dokumentet ses över minst en gång per år och uppdateras vid behov. Styrelsen tar tacksamt emot synpunkter på innehållet. Synpunkter och frågeställningar skickas till: styrelsen@rockrullarna.se</p>

