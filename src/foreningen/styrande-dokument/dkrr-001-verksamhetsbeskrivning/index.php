    <h2 class="kapitel"><a id="kap6"></a>6 Trivselregler</h2>
    
    <p><strong>Uppträdande</strong></p>
    <p>Förening är till för att skapa glädje och gemenskap. Tänk alltid efter hur ditt uppträdande är mot andra medlemmar både i föreningens lokaler och föreningens sidor på sociala medier. Vi är alla unika, har olika målsättningar, upplever, tolkar och reagerar på olika sätt. Som medlem i DK Rockrullarna behandlar du övriga medlemmar vänskapligt, respektfullt och med sunt förnuft.</p>
    <p>För mer information, se dokument: DKRR-005 Policy mot diskriminering, sexuella trakasserier och kränkande särbehandling</p>

    <p><strong>Ytter- och inneskor</strong></p>
    <p>Ytterskor får inte användas i lokalen. Ytterskor placeras på skohyllorna vid entrén. Föreningen tillåter heller inte att inneskor används på fel sida av skogränsen (vid entrén). Detta för att undvika onödigt slitage på våra dansgolv.</p>
    <p>De skor som används i lokalerna ska vara avsedda för inomhusbruk, ej sulor som ger svarta märken. Olika medel (ex. siliconspray) för att förändra skons kontaktyta mot golvet får ej användas.</p>

    <p><strong>Dansa säkert och var rädd om varandra</strong></p>
    <p>Har du en ny danspartner, exempelvis under en trivselkväll eller kurs, så ombeds paret att ta det lugnt i början. Känn efter och anpassa nivån på dansen så att ni dansar tillsammans. Försök att hålla avstånd till andra danspar och planera din dans för att undvika krockar. Alla vill gå av dansgolvet med ett leende och inte med en skada.</p>

    <p><strong>Hygien</strong></p>
    <p>Samtliga medlemmar ombeds att:</p>
    <ul>
      <li>upprätthålla god hygien</li>
      <li>av hänsyn till astmatiker och doftöverkänsliga undvika att använda parfymer, rakvatten, hårsprayer, etc.</li>
      <li>inte använda kläder som är fulla med djurhår</li>
      <li>byta om hemma</li>
      <li>tvätta/sprita händerna innan och efter kurs</li>
      <li>stanna hemma vid sjukdomssymptom.</li>
    </ul>

    <p><strong>Sopor, disk, städning</strong></p>
    <p>Städning av lokalerna (ej dansgolv) genomförs en gång i veckan via anlitat städbolag. Däremellan är det upp till samtliga medlemmar att ansvara för den gemensamma miljön.</p>
    <p>Städutrustning finns att tillgå i städskrubben i anslutning till den Lilla salen.</p>
    <p>Samtliga medlemmar ombeds att:</p>
    <ul>
      <li>plocka bort efter sig och ställa tillbaka bord och stolar</li>
      <li>ställa disk vid receptionen</li>
      <li>inte slänga matrester. Om man slänger sopor innehållande matrester skall dessa tas med och kastas utanför klubbens lokaler</li>
      <li>städa lokalen efter avslutad egenträning</li>
    </ul>

