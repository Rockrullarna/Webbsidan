    <h2 class="kapitel"><a id="kap5"></a>5 Lokal</h2>
    <p>DK Rockrullarnas lokal ägs av Örebrobostäder (ÖBO) och är lokaliserad i källaren i Haga Centrum, Vaktelvägen 2, 703 48 Örebro. Entrén ligger i anslutning till den västra sidan av köpcentrumet. Lokalen har nyttjats av DK Rockrullarna sedan 1999, har en total yta på 630 m² och inrymmer max 150 personer. Innan lokalen togs över av föreningen användes den som en övningsskjutbana där det stora säkerhetsvalvet, som i dagsläget är förråd, användes som vapenskåp.</p>
    
    <p><strong>Allmänna utrymmen</strong></p>
    <ul>
      <li>Entré/passage, två (2) toaletter och samkvämsyta med bord, stolar och soffor.</li>
    </ul>

    <p><strong>Omklädningsrum</strong></p>
    <ul>
      <li>Omklädningsrum med dusch för tränare och assistenter.</li>
    </ul>

    <p><strong>Café & reception</strong></p>
    <ul>
      <li>Café & reception är bemannad så ofta som möjligt för att ge service till föreningens medlemmar. Vid receptionens vänstra del genomförs ankomstregistrering i en ankomstterminal.</li>
      <li>I caféet kan du köpa te, kaffe, dricka samt enklare tilltugg (konditoribit, godis etc.). Betalning sker via Swish.</li>
      <li>I café & reception finns köksutrustning att tillgå så som mikrovågsugn, ugn, kyl, frys, diskmaskin och diverse köksredskap.</li>
    </ul>

    <p><strong>Förråd</strong></p>
    <ul>
      <li>Förråden används bland annat till förvaring av bord, stolar, försäljningsmateriel och materiel till dansuppvisningar.</li>
    </ul>

    <p><strong>Kontor</strong></p>
    <ul>
      <li>Kontoret används av styrelsemedlemmar och övriga roller för avskilt arbete, lagring och arkivering av dokument och data.</li>
    </ul>

    <p><strong>Städskrubb</strong></p>
    <ul>
      <li>I städskrubben finns allt som behövs för att städa lokalerna. Dörrar till utrymmet i städskrubben får ej blockeras då de ska kunna användas som utrymningsväg.</li>
    </ul>

