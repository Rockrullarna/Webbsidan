    <h2 class="kapitel"><a id="kap3" href="#kap3" class="anchor-link">🔗</a>3 Gemensamma riktlinjer och förväntningar</h2>
    <p>Nedan följer gemensamma riktlinjer och förväntningar som gäller för samtliga personer med en roll i föreningen.</p>
    
    <h3>1. Tillsammans för helheten</h3>
    <ul>
      <li>Vi har alla ett gemensamt ansvar för helheten.</li>
      <li>Vi är föreningens ansikten utåt och ser till att vara ett föredöme i vårt bemötande mot varandra, föreningens medlemmar och externa parter.</li>
      <li>Vi verkar för en välkomnande och trygg atmosfär för alla.</li>
    </ul>

    <h3>2. Kommunikation och samarbete</h3>
    <ul>
      <li>Vi visar förståelse för att ideellt arbete sker utifrån olika livssituationer.</li>
      <li>Vi är öppna för att hjälpa varandra, dela erfarenheter och bidra till en miljö där alla känner att de kan göra sin röst hörd.</li>
      <li>Vi visar respekt för andras synpunkter och bidrar till en positiv och lösningsfokuserad ton i diskussioner.</li>
      <li>Vi bidrar till att lösa eventuella konflikter eller utmaningar som kan uppstå i samarbetet.</li>
    </ul>

    <h3>3. Ta ansvar för sitt uppdrag</h3>
    <ul>
      <li>Vi är väl insatta i våra roller, arbetsprocesser och övriga styrande dokument.</li>
      <li>Vi är väl insatta i och arbetar för föreningens verksamhetsmål.</li>
      <li>Vi meddelar om aktiviteter inte kan genomföras inom utsatt tid samt ber om stöd om så behövs.</li>
      <li>Vi förbereder oss inför möten och aktiviteter så att vi använder vår gemensamma tid effektivt.</li>
      <li>Vi deltar på möten som är kopplade till våra ansvarsroller.</li>
    </ul>

    <h3>4. Utveckling och lärande</h3>
    <ul>
      <li>Vi lär av varandra och hjälps åt. Det är okej att inte kunna allt.</li>
      <li>Vi delar gärna idéer och kommer med förslag på förbättringar.</li>
    </ul>
