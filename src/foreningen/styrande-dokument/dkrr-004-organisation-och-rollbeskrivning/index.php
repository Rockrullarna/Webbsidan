    <h2 class="kapitel"><a id="kap4" href="#kap4" class="anchor-link">🔗</a>4 Roll- och uppdragsbeskrivning</h2>
    <h3 class="section"><a id="kap4-1" href="#kap4-1" class="anchor-link">🔗</a>4.1 Styrelse</h3>
    <p>Styrelsen har det övergripande ansvaret att leda och utveckla föreningens verksamhet i enlighet med föreningens stadgar och verksamhetsmål samt styrande direktiv från Riksidrottsförbundet och Danssportsförbundet. Styrelsens är förtroendevalda av medlemmarna och ska verka för att skapa en trygg, inkluderande och inspirerande miljö där medlemmar i alla åldrar och nivåer kan utöva dans och delta i föreningslivet.</p>

    <h4>Övergripande ansvar</h4>
    <ul>
      <li>Ansvarar för att sätta mål och riktlinjer för föreningens baserat på föreningens egen behov samt styrande direktiv från Riksidrottsförbundet och Danssportsförbundet.</li>
      <li>Fattar beslut i frågor som rör ekonomi, verksamheten och organisation mellan årsmöten.</li>
      <li>Representerar föreningen gentemot externa parter som kommuner, förbund, sponsorer och samarbetspartner.</li>
      <li>Följer upp och utvärderar verksamhetsplaner, ekonomi och måluppfyllelse.</li>
    </ul>

    <h4>Ekonomiskt ansvar</h4>
    <ul>
      <li>Upprätta och följa budget samt säkerställa att föreningens resurser används ansvarsfullt.</li>
      <li>Ansvara för att bokföring och redovisning sker enligt gällande lagar och regler.</li>
      <li>Förbereda och presentera ekonomisk rapport och årsredovisning inför årsmötet.</li>
    </ul>

    <h4>Organisatoriskt ansvar</h4>
    <ul>
      <li>Fördela roller och arbetsuppgifter inom styrelsen samt säkerställa att dessa fullföljs.</li>
      <li>Ta fram och vid behov uppdatera styrande dokumentation.</li>
      <li>Säkerställa medlemsinflytande och en god kommunikation inom föreningen.</li>
    </ul>

    <h4>Verksamhetsansvar</h4>
    <ul>
      <li>Planera och följa upp dansverksamheten så att den är kvalitativ och i linje med föreningens värdegrunder.</li>
      <li>Arbeta aktivt med att rekrytera och stötta föreningens organisation.</li>
      <li>Främja utveckling av dansare på olika nivåer – från nybörjare till tävlingsdeltagare.</li>
    </ul>
    <p>&nbsp;</p>

    <h4><a id="kap4-1-1" href="#kap4-1-1" class="anchor-link">🔗</a>4.1.1 Firmatecknare</h4>
    <h5>Uppdragsbeskrivning</h5>
    <p>Föreningens firmatecknare är huvudansvarig att, vid behov, skriva under olika typer av kontrakt och avtal i föreningens namn. Exempelvis kan detta handla om att skriva under hyresavtal, bidragsansökan samt hantera föreningens bankärenden. Det är styrelsens uppgift att utse föreningens firmatecknare och är en roll som ska delges någon lämplig person inom styrelsen. Som firmatecknare arbetar du tätt med styrelsen och har ingen rätt att fatta egna beslut utan styrelsens godkännande.</p>
    <h5>Huvudsakliga uppgifter</h5>
    <ul>
      <li>Företräda föreningen juridiskt.</li>
      <li>Skriva under avtal och kontrakt i föreningens namn, t.ex. lokaler, samarbetspartners och leverantörer.</li>
      <li>Skriva under handlingar gentemot myndigheter, exempelvis Skatteverket och kommunen.</li>
      <li>Öppna och hantera bankkonton, skriva under transaktioner och ha tillgång till föreningens ekonomi.</li>
      <li>Följa riktlinjer och förväntningar i kapitel 3.</li>
    </ul>
    <h5>Kunskapskrav</h5>
    <ul>
      <li>Grundläggande utbildning i styrelsearbete.</li>
    </ul>
    <p>&nbsp;</p>

    <h4><a id="kap4-1-2" href="#kap4-1-2" class="anchor-link">🔗</a>4.1.2 Ordförande</h4>
    <h5>Uppdragsbeskrivning</h5>
    <p>Ordförande är föreningens huvudrepresentant och ansvarar för att leda styrelsens arbete. Uppgifter som ingår i rollen är bland annat att säkerställa att föreningens verksamhet bedrivs enligt stadgar, beslut och lagar, att sammankalla och leda styrelsemöten och att delegera och följa upp arbetsuppgifter.</p>
    <p>Ordförande ansvarar även för granskning och inregistrering av registerutdrag för tränare/assistenter/hjälpdansare samt att registerutdraget hanteras på ett korrekt sätt. Registerutdraget får aldrig sparas inom föreningen utan ska lämnas tillbaka till berörd person eller förstöras enligt överenskommelse.</p>
    <h5>Huvudsakliga uppgifter</h5>
    <ul>
      <li>Huvudrepresentant för föreningen.</li>
      <li>Leder styrelsens arbete, fördelar arbetsuppgifter och sammankallar till styrelsemöten.</li>
      <li>Följer upp styrelsens arbetsuppgifter och säkerställer att styrelsens beslut verkställs.</li>
      <li>Säkerställer att föreningens verksamhet bedrivs enligt stadgar, beslut och lagar.</li>
      <li>Stödjer och koordinerar övriga styrelsemedlemmars arbete.</li>
      <li>Granskar och genomför inregistrering av registerkontroller (idrott online).</li>
      <li>Följa riktlinjer och förväntningar i kapitel 3.</li>
    </ul>
    <h5>Kunskapskrav</h5>
    <ul>
      <li>Utbildning idrott online.</li>
      <li>Grundläggande utbildning i styrelsearbete.</li>
      <li>Utbildning i föreningens teknik och IT-miljö.</li>
    </ul>
